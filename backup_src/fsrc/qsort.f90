        A(i)%value = random
        A(i)%order = i
        if (mod(i,4) == 0) write (*,"(4(I5,1X,F8.6))") A(i-3:i)
    end do
 
    call QSort(A,l)
    write (*,*) "Sorted Values:"
    do i = 1,l
      write (*,"(I5,1X,F8.6,1x,I5)") i, A(i)%value, A(i)%order
    end do
 
    write (*,"(I5,1X,F8.6)") 4, A(4)%value
end program qsort_test
